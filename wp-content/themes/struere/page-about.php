        <p><span class="hraztan__name">Hraztan Zeitlian</span> is the Founder and Design Principal of STRUERE Architecture, Los Angeles. He leads design conceptualization and strategy, in addition to overseeing all aspects of the practice. Hraztan brings high-energy design leadership focused on creating Award Winning, sophisticated/innovative design solutions, with a strong collaborative connection to multiple Market Sectors, Disciplines, management goals and technical execution. Prior to Struere, Hraztan was Design Leader of DLR’s Los Angeles Office, Director of Design of Leo A Daly's Los Angeles Office, and was Director of Design of Genslers Los Angeles offices Entertainment Studio 1. His work experience includes SOM and JohnsonFain.</p>
