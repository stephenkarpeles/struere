        <p>Hraztan takes leadership in disseminating the great work developed in the office under his design leadership with speaking engagements. In 2011, he was a presenter at the Los Angeles Community College District 9x9x9 Conference. Also in 2011, he gave a Lecture at Woodbury University about the California Architects Practice Act. In 2009, he was a panelist at the Los Angeles AIA Design Build RoundTable. In 2008, he was a Panelist at the AIA Los Angeles / UCLA Event: In the Academic Realm.</p>
